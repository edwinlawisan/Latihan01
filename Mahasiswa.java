class Mahasiswa {
    private String nama;
    private String npm;

    public void setNama(String nama){
        this.nama = nama;
    }

    public void setNPM(String npm){
        this.npm = npm;
    }

    public String getNama(){
        return this.nama;
    }

    public String getNPM(){
        return this.npm;
    }
}