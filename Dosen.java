class Dosen {
    private String nama;
    private String mataKuliah;

    public void setNama(String nama){
        this.nama = nama;
    }

    public void setMataKuliah(String mataKuliah){
        this.mataKuliah = mataKuliah;
    }

    public String getNama(){
        return this.nama;
    }

    public String getMataKuliah(){
        return this.mataKuliah;
    }
}